// pyTool is Python's empirical surface (or-4y7.9, library tracer): staging is
// the shared tree copy; the unit channel synthesizes a python driver
// (case:segment dispatch, mirroring the Go driver's restart-boundary protocol)
// and runs each segment as a fresh sandboxed interpreter process — a real
// process boundary, so restart steps prove persistence exactly like Go. The
// http/exec channels refuse loudly: the tracer proves libraries; a runnable
// python service (argv-shaped, not a compiled binary) is the next
// generalization, and pretending with a stub would be a meaningless probe.
type pyTool struct{}

func (pyTool) Language() string { return "python" }

func (pyTool) Stage(src, dst string) error { return pyStage(src, dst) }

// pyStage copies the python artifact tree into the proof-controlled dir:
// production *.py (and asset files), excluding bytecode caches, dot-dirs, and
// any prior harness-authored corpus/driver (orion_*). The shared stageArtifact
// is Go-shaped (it requires buildable .go sources), so python stages its own.
func pyStage(src, dst string) error {
	staged := 0
	err := filepath.WalkDir(src, func(p string, d os.DirEntry, werr error) error {
		if werr != nil {
			return werr
		}
		rel, rerr := filepath.Rel(src, p)
		if rerr != nil {
			return rerr
		}
		if rel == "." {
			return nil
		}
		name := d.Name()
		if d.IsDir() {
			if strings.HasPrefix(name, ".") || name == "__pycache__" {
				return filepath.SkipDir
			}
			return os.MkdirAll(filepath.Join(dst, rel), 0o755)
		}
		if strings.HasPrefix(name, "orion_") || strings.HasSuffix(name, ".pyc") {
			return nil
		}
		data, rerr := os.ReadFile(p)
		if rerr != nil {
			return rerr
		}
		if strings.HasSuffix(name, ".py") {
			staged++
		}
		return os.WriteFile(filepath.Join(dst, rel), data, 0o644)
	})
	if err != nil {
		return err
	}
	if staged == 0 {
		return fmt.Errorf("no python sources in %s", src)
	}
	return nil
}

