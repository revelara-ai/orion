// pyDriverIdentRE guards module names interpolated into the generated driver.
var pyDriverIdentRE = regexp.MustCompile(`^[A-Za-z_][A-Za-z0-9_]*(\.[A-Za-z_][A-Za-z0-9_]*)*$`)

func (pyTool) BuildUnitDriver(_ context.Context, binDir string, unitCases []spec.BehavioralCase) (string, bool, string, error) {
	var b strings.Builder
	b.WriteString("# Code generated by Orion proof/test-synthesis. DO NOT EDIT.\nimport importlib\nimport re\nimport sys\n\n\ndef fail(msg):\n    print(msg, file=sys.stderr)\n    sys.exit(1)\n\n\ndef run(key):\n")
	wrote := false
	for _, cs := range unitCases {
		if cs.Kind != spec.KindUnit || cs.Unit == nil {
			continue
		}
		mod := strings.TrimSpace(cs.Unit.Pkg)
		if mod == "" {
			fmt.Fprintf(&b, "    if key == %q:\n        fail(%q)\n", cs.ID+":0",
				"unit case "+cs.ID+" declares no importable module (pkg) — name the package the case calls")
			wrote = true
			continue
		}
		if !pyDriverIdentRE.MatchString(mod) {
			return "", false, fmt.Sprintf("unit case %s: pkg %q is not an importable module name", cs.ID, cs.Unit.Pkg), nil
		}
		seg := 0
		emitHead := func() {
			fmt.Fprintf(&b, "    if key == %q:\n", fmt.Sprintf("%s:%d", cs.ID, seg))
			fmt.Fprintf(&b, "        m = importlib.import_module(%q)\n        env = dict(vars(m))\n", mod)
		}
		emitHead()
		for i, st := range cs.Unit.Steps {
			if st.Restart {
				b.WriteString("        return\n")
				seg++
				emitHead()
			}
			if st.WantErrRE != "" {
				fmt.Fprintf(&b, "        try:\n            eval(compile(%q, '<case>', 'eval'), env)\n", st.Call)
				fmt.Fprintf(&b, "        except Exception as e:\n")
				fmt.Fprintf(&b, "            if not re.search(%q, str(e)):\n                fail('step %d: error %%r does not match %%r' %% (str(e), %q))\n", st.WantErrRE, i+1, st.WantErrRE)
				fmt.Fprintf(&b, "        else:\n            fail('step %d: the call was required to raise')\n", i+1)
			} else {
				fmt.Fprintf(&b, "        got = eval(compile(%q, '<case>', 'eval'), env)\n", st.Call)
				fmt.Fprintf(&b, "        want = eval(compile(%q, '<want>', 'eval'), {})\n", st.Want)
				fmt.Fprintf(&b, "        if got != want:\n            fail('step %d: got %%r, want %%r' %% (got, want))\n", i+1)
			}
		}
		b.WriteString("        return\n")
		wrote = true
	}
	if !wrote {
		return "", false, "no unit cases", nil
	}
	b.WriteString("    print('unknown case/segment', file=sys.stderr)\n    sys.exit(2)\n\n\nif __name__ == '__main__':\n    if len(sys.argv) != 3:\n        print('usage: driver <caseID> <segment>', file=sys.stderr)\n        sys.exit(2)\n    run(sys.argv[1] + ':' + sys.argv[2])\n")
	p := filepath.Join(binDir, "orion_unit_driver.py")
	if err := os.WriteFile(p, []byte(b.String()), 0o644); err != nil {
		return "", false, "", err
	}
	return p, true, "", nil
}

func (pyTool) UnitRound(ctx context.Context, driver string, unitCases []spec.BehavioralCase) (map[string]truthalign.ObligationStatus, string) {
	obs := map[string]truthalign.ObligationStatus{}
	var fails []string
	dir := filepath.Dir(driver)
	base := filepath.Base(driver)
	for _, cs := range unitCases {
		if cs.Kind != spec.KindUnit || cs.Unit == nil {
			continue
		}
		pass, detail := true, ""
		for seg := 0; seg < testsynth.UnitSegments(cs); seg++ {
			stdout, stderr, code, err := proofexec.RunTool(ctx, dir, "python", "python3", base, cs.ID, fmt.Sprint(seg))
			if err != nil {
				pass, detail = false, fmt.Sprintf("segment %d: %v", seg, err)
				break
			}
			if code != 0 {
				pass, detail = false, fmt.Sprintf("segment %d: %s", seg, strings.TrimSpace(stdout+stderr))
				break
			}
		}
		obs[cs.ID] = truthalign.ObligationStatus{Executed: true, Passed: pass}
		if !pass {
			fails = append(fails, cs.ID+": "+detail)
		}
	}
	return obs, strings.Join(fails, "; ")
}

